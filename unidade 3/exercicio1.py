L = [5, 7, 2, 9, 4, 1, 3]

print("Quantidade de elementos na lista:", len(L))

print("Maior valor na lista:", max(L))

print("Menor valor na lista:", min(L))

print("Soma de todos os elementos:", sum(L))

print("Lista em ordem crescente:", sorted(L))

print("Lista em ordem decrescente:", sorted(L, reverse=True))
